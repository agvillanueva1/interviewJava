package JavaChallenges;

public class PositiveOrNegativeNumber {
    public static void main(String[] args) {

        double number = 0;

        if(number < 0.0){
            System.out.println(number + " negative");
        }else if(number > 0.0){
            System.out.println(number + " positive");
        }else{
            System.out.println(number + " is zero");
        }

    }
}
